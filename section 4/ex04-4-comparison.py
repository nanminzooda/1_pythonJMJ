'''

관계연산자
    두 개의 항을 비교하여 그 결과를 논리(불) 자료형으로 반환
'''

# 트루와 펄쓰

num=15

print('{} > 10 : {}'.format(num,num>10))
print('{} < 10 : {}'.format(num,num<10))
print('{} >= 10 : {}'.format(num,num>=10))
print('{} <= 10 : {}'.format(num,num<=10))
print('{} == 10 : {}'.format(num,num==10))
print('{} != 10 : {}'.format(num,num!=10))